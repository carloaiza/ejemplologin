    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        // TODO add your handling code here:

        if (txtCorreo.getText() == null || txtCorreo.getText().compareTo("") == 0) {
            JOptionPane.showMessageDialog(this,
                    "Debe diligenciar el correo", "Datos Faltantes", 2);
        } else if (txtContrasenia.getPassword().length == 0) {
            JOptionPane.showMessageDialog(this,
                    "Debe diligenciar la contrase침a", "Datos Faltantes", 2);
        } else {
            controlLogin = new ControladorLogin();

            //Significa que diligencio correo y contrase침a
            Pattern pattern = Pattern.compile("^[a-zA-Z0-9_!#$%&'*+/=?`{|}~^.-]+@[a-zA-Z0-9.-]+$");
            Matcher matcher = pattern.matcher(txtCorreo.getText());

            if (matcher.find() == true) {
                usuarioAutenticado = controlLogin.
                        encontrarUsuario(txtCorreo.getText(), txtContrasenia.getText());
                if (usuarioAutenticado == null) {
                    JOptionPane.showMessageDialog(this,
                            "El usuario Ingresado no existe", "Datos Err칩neos", 0);
                } else {
                    controlUniversidad = new ControladorUniversidad();

                    JOptionPane.showMessageDialog(this,
                            "Bienvenido " + usuarioAutenticado, "Bienvenido", 1);
                    mnuEjercicioLogin.setEnabled(true);
                    gestionarPermisosMenu();
                    jfrmLogin.hide();
                }
            } else {
                JOptionPane.showMessageDialog(this,
                        "El correo no tiene un formato v치lido", "Datos Err칩neos", 0);

            }

        }
    }//GEN-LAST:event_btnIngresarActionPerformed

